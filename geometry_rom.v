    rom[8'hBC] = 32'hA8_28_0000; // front  : 강한 scatter, 중간 흡수
    rom[8'hBD] = 32'h08_A8_0000; // back   : 최소 scatter, 강한 흡수
end

// 동기식 read
always @(posedge clk) begin
    dout <= rom[addr];
end

endmodule
