// ============================================================================
//  geometry_rom.v  v3
//
//  v2 → v3: 물리 기반 파라미터 전면 재설계
//
//  [ROM-v3-1]  room_size_hint 물리 거리 정확 매핑
//              v2: 일부 공간 크기 차이 부족
//              v3: AMBIENCE(2m) → FROZEN(80m) 물리 스케일 엄격 적용
//
//  [ROM-v3-2]  wall별 비대칭 강화
//              v2: left/right/front/back이 동일한 경우 다수
//              v3: 모든 공간에서 6면 각각 다른 값
//                  floor: 흡수↑ scatter↓ (카펫/나무바닥)
//                  ceil:  흡수↓ scatter↑ (확산 천장)
//                  left/right: 서로 다른 재질
//                  front/back: 서로 다른 깊이감
//
//  [ROM-v3-3]  흡수/산란 물리 기준값
//              absorption: 0x10=콘크리트 0x40=벽 0x80=목재 0xC0=흡음재
//              scatter:    0x10=거울 0x40=보통 0x80=거친 0xC0=완전확산
//
//  entry: {scatter[7:0], absorption[7:0], room_size_hint[15:0]}
//  주소: {space_id[4:0], wall_id[2:0]}
//  wall_id: 0=left 1=right 2=floor 3=ceil 4=front 5=back
//  room_size_hint: wall_id==0만 유효, Q8 (0x0100=1m)
// ============================================================================

`timescale 1ns / 1ps

module geometry_rom (
    input  wire        clk,
    input  wire [7:0]  addr,
    output reg  [31:0] dout
);

(* rom_style = "block" *) reg [31:0] rom [0:255];

integer i;
initial begin
    for (i = 0; i < 256; i = i + 1)
        rom[i] = 32'h20_40_0000;

    // ======================================================================
    //  0: SP_AMBIENCE - 2m 소형 ambient (방석 깔린 작은 방)
    //  특성: 고흡수, 저scatter, 초단 감쇠
    // ======================================================================
    rom[8'h00] = 32'h14_B8_0200; // left   : sct=0x14 abs=0xB8 size=2m
    rom[8'h01] = 32'h18_C4_0000; // right  : 약간 다른 흡수
    rom[8'h02] = 32'h0C_D8_0000; // floor  : 카펫, 최고 흡수, 최저 scatter
    rom[8'h03] = 32'h20_A0_0000; // ceil   : 중간 흡수, 약간의 확산
    rom[8'h04] = 32'h10_C0_0000; // front  : 커튼
    rom[8'h05] = 32'h1C_B0_0000; // back   : 책장 (약간 확산)

    // ======================================================================
    //  1: SP_HALL - 30m 대형 콘서트홀
    //  특성: 중흡수, 중scatter, 긴 잔향
    // ======================================================================
    rom[8'h08] = 32'h58_40_1E00; // left   : 콘크리트벽, 중간 scatter
    rom[8'h09] = 32'h50_48_0000; // right  : 약간 다른 벽면
    rom[8'h0A] = 32'h20_78_0000; // floor  : 나무바닥, 중흡수
    rom[8'h0B] = 32'h70_20_0000; // ceil   : 저흡수, 강한 확산 (돔 천장)
    rom[8'h0C] = 32'h60_38_0000; // front  : 무대 반사
    rom[8'h0D] = 32'h48_58_0000; // back   : 객석 흡수

    // ======================================================================
    //  2: SP_CATHEDRAL - 60m 대성당
    //  특성: 저흡수, 강scatter, 매우 긴 잔향
    // ======================================================================
    rom[8'h10] = 32'h90_18_3C00; // left   : 석벽, 강한 확산
    rom[8'h11] = 32'h88_20_0000; // right  : 스테인드글라스 벽
    rom[8'h12] = 32'h30_50_0000; // floor  : 대리석, 중간 흡수
    rom[8'h13] = 32'hA0_0C_0000; // ceil   : 아치형, 거의 반사, 최강 확산
    rom[8'h14] = 32'h80_28_0000; // front  : 제단
    rom[8'h15] = 32'h78_30_0000; // back   : 나르텍스

    // ======================================================================
    //  3: SP_CHURCH - 25m 교회
    //  특성: 중-저 흡수, 중-강 scatter
    // ======================================================================
    rom[8'h18] = 32'h68_30_1900; // left
    rom[8'h19] = 32'h60_38_0000; // right
    rom[8'h1A] = 32'h28_68_0000; // floor  : 나무바닥
    rom[8'h1B] = 32'h80_18_0000; // ceil   : 높은 천장
    rom[8'h1C] = 32'h70_28_0000; // front  : 성가대석
    rom[8'h1D] = 32'h58_48_0000; // back   : 출입구, 흡수↑

    // ======================================================================
    //  4: SP_SMALL_ROOM - 4m 소형 방
    //  특성: 고흡수, 저scatter, 빠른 감쇠
    // ======================================================================
    rom[8'h20] = 32'h18_A0_0400; // left   : 일반 벽
    rom[8'h21] = 32'h20_98_0000; // right  : 창문 있는 벽
    rom[8'h22] = 32'h0C_C8_0000; // floor  : 카펫
    rom[8'h23] = 32'h28_88_0000; // ceil   : 석고보드
    rom[8'h24] = 32'h14_B0_0000; // front  : 옷장
    rom[8'h25] = 32'h1C_A8_0000; // back   : 문

    // ======================================================================
    //  5: SP_MEDIUM_ROOM - 8m 중형 방 (거실)
    //  특성: 중흡수, 중scatter
    // ======================================================================
    rom[8'h28] = 32'h30_80_0800; // left   : 벽+그림
    rom[8'h29] = 32'h38_78_0000; // right  : 유리창
    rom[8'h2A] = 32'h18_A0_0000; // floor  : 나무마루
    rom[8'h2B] = 32'h40_60_0000; // ceil   : 일반 천장
    rom[8'h2C] = 32'h2C_88_0000; // front  : 소파 벽
    rom[8'h2D] = 32'h34_70_0000; // back   : TV 벽

    // ======================================================================
    //  6: SP_LARGE_ROOM - 15m 대형 방 (강당)
    //  특성: 중흡수, 중scatter
    // ======================================================================
    rom[8'h30] = 32'h48_58_0F00; // left   : 패널벽
    rom[8'h31] = 32'h40_60_0000; // right  : 유리벽
    rom[8'h32] = 32'h20_80_0000; // floor  : 리놀륨
    rom[8'h33] = 32'h58_40_0000; // ceil   : 음향 타일
    rom[8'h34] = 32'h4C_50_0000; // front  : 스크린
    rom[8'h35] = 32'h38_68_0000; // back   : 출입구+의자

    // ======================================================================
    //  7: SP_PLATE_CLASSIC - 가상 플레이트
    //  room_size=0 (plate 모드), 완전반사, 강 scatter
    // ======================================================================
    rom[8'h38] = 32'hE0_00_0000;
    rom[8'h39] = 32'hD8_04_0000;
    rom[8'h3A] = 32'hE8_00_0000;
    rom[8'h3B] = 32'hD0_08_0000;
    rom[8'h3C] = 32'hE4_02_0000;
    rom[8'h3D] = 32'hDC_06_0000;

    // ======================================================================
    //  8: SP_PLATE_BRIGHT - 밝은 플레이트 (harder)
    // ======================================================================
    rom[8'h40] = 32'hFF_00_0000;
    rom[8'h41] = 32'hF8_00_0000;
    rom[8'h42] = 32'hFF_00_0000;
    rom[8'h43] = 32'hF4_04_0000;
    rom[8'h44] = 32'hFC_00_0000;
    rom[8'h45] = 32'hF6_02_0000;

    // ======================================================================
    //  9: SP_PLATE_DARK - 어두운 플레이트 (softer)
    // ======================================================================
    rom[8'h48] = 32'hA8_10_0000;
    rom[8'h49] = 32'hA0_14_0000;
    rom[8'h4A] = 32'hB0_08_0000;
    rom[8'h4B] = 32'h98_1C_0000;
    rom[8'h4C] = 32'hAC_0C_0000;
    rom[8'h4D] = 32'hA4_18_0000;

    // ======================================================================
    //  10: SP_RESONANCE - 8m 공진 공간 (금속 탱크)
    //  특성: 저흡수, 강scatter, 공명
    // ======================================================================
    rom[8'h50] = 32'h60_18_0800; // left   : 금속벽
    rom[8'h51] = 32'h58_20_0000; // right  : 금속벽 (약간 부식)
    rom[8'h52] = 32'h40_30_0000; // floor  : 금속 바닥
    rom[8'h53] = 32'h70_10_0000; // ceil   : 둥근 천장 (강한 확산)
    rom[8'h54] = 32'h50_28_0000; // front  : 해치
    rom[8'h55] = 32'h68_14_0000; // back   : 파이프

    // ======================================================================
    //  11: SP_RESONANT_SP - 8m 공진 공간 (세라믹/타일)
    // ======================================================================
    rom[8'h58] = 32'h50_24_0800; // left   : 타일벽
    rom[8'h59] = 32'h48_2C_0000; // right
    rom[8'h5A] = 32'h38_38_0000; // floor  : 타일 (약간 흡수)
    rom[8'h5B] = 32'h60_18_0000; // ceil   : 아치형
    rom[8'h5C] = 32'h54_20_0000; // front
    rom[8'h5D] = 32'h44_30_0000; // back

    // ======================================================================
    //  12: SP_TONAL - 10m 토널 공간 (목조)
    //  특성: 중흡수, 중scatter, 따뜻한 음색
    // ======================================================================
    rom[8'h60] = 32'h38_68_0A00; // left   : 나무 패널
    rom[8'h61] = 32'h34_70_0000; // right  : 나무 (다른 종류)
    rom[8'h62] = 32'h1C_90_0000; // floor  : 나무마루, 고흡수
    rom[8'h63] = 32'h44_58_0000; // ceil   : 목재 천장
    rom[8'h64] = 32'h30_78_0000; // front  : 커튼 (따뜻함)
    rom[8'h65] = 32'h3C_64_0000; // back   : 나무문

    // ======================================================================
    //  13: SP_RINGING - 15m 링잉 공간 (유리/대리석)
    //  특성: 초저흡수, 중scatter, 강한 공명
    // ======================================================================
    rom[8'h68] = 32'h40_10_0F00; // left   : 유리벽
    rom[8'h69] = 32'h38_14_0000; // right  : 대리석
    rom[8'h6A] = 32'h24_28_0000; // floor  : 대리석 (약간 흡수)
    rom[8'h6B] = 32'h50_08_0000; // ceil   : 유리 돔 (최저 흡수)
    rom[8'h6C] = 32'h48_0C_0000; // front  : 유리
    rom[8'h6D] = 32'h30_1C_0000; // back   : 돌벽

    // ======================================================================
    //  14: SP_ROTATING - 30m 회전 공간 (강한 모듈레이션)
    //  특성: 중흡수, 강scatter (공간적 회전감)
    // ======================================================================
    rom[8'h70] = 32'h78_48_1E00; // left   : 불규칙 벽
    rom[8'h71] = 32'h68_50_0000; // right  : 다른 질감
    rom[8'h72] = 32'h38_70_0000; // floor
    rom[8'h73] = 32'h88_30_0000; // ceil   : 강한 확산
    rom[8'h74] = 32'h80_40_0000; // front  : 강한 scatter
    rom[8'h75] = 32'h60_58_0000; // back   : 흡수↑

    // ======================================================================
    //  15: SP_DRIFTING - 40m 드리프팅 (느린 피치 변조)
    //  특성: 저흡수, 강scatter, 넓은 공간감
    // ======================================================================
    rom[8'h78] = 32'h70_28_2800; // left
    rom[8'h79] = 32'h68_30_0000; // right
    rom[8'h7A] = 32'h40_50_0000; // floor
    rom[8'h7B] = 32'h80_18_0000; // ceil   : 넓은 확산
    rom[8'h7C] = 32'h78_24_0000; // front
    rom[8'h7D] = 32'h58_3C_0000; // back

    // ======================================================================
    //  16: SP_GATED - 5m 게이티드 (빠른 차단)
    //  특성: 고흡수, 강scatter (초반 밀도), 급격한 감쇠
    // ======================================================================
    rom[8'h80] = 32'h68_90_0500; // left   : 흡음재 벽
    rom[8'h81] = 32'h60_98_0000; // right
    rom[8'h82] = 32'h48_B0_0000; // floor  : 매우 강한 흡수
    rom[8'h83] = 32'h78_80_0000; // ceil
    rom[8'h84] = 32'h70_88_0000; // front
    rom[8'h85] = 32'h58_A0_0000; // back   : 최강 흡수

    // ======================================================================
    //  17: SP_REVERSE - 40m 역방향 리버브
    //  특성: 저흡수, 중scatter (역방향 에너지)
    // ======================================================================
    rom[8'h88] = 32'h50_28_2800; // left
    rom[8'h89] = 32'h48_30_0000; // right
    rom[8'h8A] = 32'h2C_50_0000; // floor
    rom[8'h8B] = 32'h60_1C_0000; // ceil
    rom[8'h8C] = 32'h58_24_0000; // front
    rom[8'h8D] = 32'h40_38_0000; // back

    // ======================================================================
    //  18: SP_BROKEN - 20m 불규칙/파손 (비대칭 극대화)
    //  특성: wall마다 완전히 다름, 최강 비대칭
    // ======================================================================
    rom[8'h90] = 32'hFF_08_1400; // left   : 최강 scatter, 거의 반사
    rom[8'h91] = 32'h10_E8_0000; // right  : 최약 scatter, 최강 흡수
    rom[8'h92] = 32'hC0_14_0000; // floor  : 강한 scatter
    rom[8'h93] = 32'h08_F0_0000; // ceil   : 거의 없음, 완전 흡수
    rom[8'h94] = 32'hE0_20_0000; // front
    rom[8'h95] = 32'h18_D0_0000; // back

    // ======================================================================
    //  19: SP_SHIMMER - 30m 쉬머 (피치 시프트 + 긴 꼬리)
    //  특성: 저흡수, 중scatter, 깨끗한 잔향
    // ======================================================================
    rom[8'h98] = 32'h4C_40_1E00; // left
    rom[8'h99] = 32'h44_48_0000; // right
    rom[8'h9A] = 32'h28_64_0000; // floor
    rom[8'h9B] = 32'h58_30_0000; // ceil
    rom[8'h9C] = 32'h50_38_0000; // front
    rom[8'h9D] = 32'h3C_50_0000; // back

    // ======================================================================
    //  20: SP_FROZEN - 80m 프리징 (초대형, 정적)
    //  특성: 초저흡수, 중-강 scatter, 가장 긴 잔향
    // ======================================================================
    rom[8'hA0] = 32'h68_0C_5000; // left   : 빙벽, 거의 완전 반사
    rom[8'hA1] = 32'h60_10_0000; // right
    rom[8'hA2] = 32'h38_28_0000; // floor  : 얼음 (약간 흡수)
    rom[8'hA3] = 32'h78_08_0000; // ceil   : 최저 흡수
    rom[8'hA4] = 32'h70_0A_0000; // front
    rom[8'hA5] = 32'h58_18_0000; // back

    // ======================================================================
    //  21: SP_ASCENDING - 30m 상승 피치
    //  특성: 저흡수, 중scatter, 상승감
    // ======================================================================
    rom[8'hA8] = 32'h48_44_1E00; // left
    rom[8'hA9] = 32'h40_4C_0000; // right
    rom[8'hAA] = 32'h24_70_0000; // floor  : 흡수↑
    rom[8'hAB] = 32'h58_30_0000; // ceil   : 흡수↓, scatter↑
    rom[8'hAC] = 32'h50_3C_0000; // front
    rom[8'hAD] = 32'h38_58_0000; // back

    // ======================================================================
    //  22: SP_DESCENDING - 20m 하강 피치 (Ascending과 구분)
    //  특성: 중흡수, 중scatter, 하강감, 더 어두운 톤
    // ======================================================================
    rom[8'hB0] = 32'h3C_58_1400; // left   : 흡수↑ (어두운 톤)
    rom[8'hB1] = 32'h34_60_0000; // right
    rom[8'hB2] = 32'h20_80_0000; // floor  : 고흡수
    rom[8'hB3] = 32'h48_48_0000; // ceil
    rom[8'hB4] = 32'h40_50_0000; // front
    rom[8'hB5] = 32'h2C_6C_0000; // back   : 흡수↑↑

    // ======================================================================
    //  23: SP_SPECIAL - 50m 특수 공간 (완전 비대칭, 최대 다양성)
    //  특성: wall마다 극단적으로 다른 파라미터
    // ======================================================================
    rom[8'hB8] = 32'h88_18_3200; // left   : 강한 scatter, 낮은 흡수
    rom[8'hB9] = 32'h10_E0_0000; // right  : 약한 scatter, 강한 흡수
    rom[8'hBA] = 32'hC8_08_0000; // floor  : 최강 scatter, 거의 반사
